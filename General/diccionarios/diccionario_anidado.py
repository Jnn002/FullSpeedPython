# Podemos tener un diccionario anidado, 
# es decir, un diccionario dentro de otro diccionario
# Ejemplo:
estudiantes = {
    'Peter': {
        'age': 10, 
        'address': 'Lisbon'
    },
    'Isabel': 
        {'age': 11, 
        'address': 'Sesimbra'
    },
    'Anna': 
        {'age': 9, 
        'address': 'Lisbon'
    },
}
print(estudiantes)